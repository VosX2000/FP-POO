    private boolean guardarProveedor(String proveedor, String razonSocial, String telefono, String descripción) {
        File proveedoresFile = new File("resources/proveedores.txt");
        File tempFile = new File("resources/proveedores_temp.txt");

        try (BufferedReader reader = new BufferedReader(new FileReader(proveedoresFile)); BufferedWriter writer = new BufferedWriter(new FileWriter(tempFile))) {

            String currentLine;
            boolean proveedorExistente = false;

            while ((currentLine = reader.readLine()) != null) {
                if (currentLine.startsWith("Proveedor: " + proveedor)) {
                    proveedorExistente = true;
                    // Actualizar los campos si el proveedor ya existe
                    writer.write("Proveedor: " + proveedor);
                    writer.newLine();
                    writer.write("Razón Social: " + razonSocial);
                    writer.newLine();
                    writer.write("Teléfono: " + telefono);
                    writer.newLine();
                    writer.write("Descripción: " + descripción);
                    writer.newLine();
                    writer.write("------------------------------");
                    writer.newLine();
                } else {
                    writer.write(currentLine);
                    writer.newLine();
                }
            }

            if (!proveedorExistente) {
                // Agregar un nuevo proveedor si no existe
                writer.write("Proveedor: " + proveedor);
                writer.newLine();
                writer.write("Razón Social: " + razonSocial);
                writer.newLine();
                writer.write("Teléfono: " + telefono);
                writer.newLine();
                writer.write("Descripción: " + descripción);
                writer.newLine();
                writer.write("------------------------------");
                writer.newLine();
            }

        } catch (IOException e) {
            e.printStackTrace();
            return false;
        }

        // Reemplazar el archivo original con el archivo temporal actualizado
        if (proveedoresFile.delete()) {
            tempFile.renameTo(proveedoresFile);
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo actualizar el archivo de proveedores.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }

