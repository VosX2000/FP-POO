    private void jButton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_guardarActionPerformed
        // TODO add your handling code here:
        String proveedor = txt_nombre.getText();
        String razonSocial = txt_cantidad.getText();
        String telefono = txt_precio.getText();
        String descripción = txt_descripción.getText();

        // Validar que los campos no estén vacíos
        if (proveedor.isEmpty() || razonSocial.isEmpty() || telefono.isEmpty() || descripción.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (guardarProveedor(proveedor, razonSocial, telefono, descripción)) {
            JOptionPane.showMessageDialog(this, "Proveedor guardado exitosamente.");
            txt_nombre.setText("");
            txt_cantidad.setText("");
            txt_precio.setText("");
            txt_descripción.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Error al guardar el proveedor.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton_guardarActionPerformed

